//Utiliza-se a estrutura de repetição "for" para percorrer uma quantidade específica de vezes

const nome = 'Adson Mozart Santos Paixão';

//Exibe a letra que está na posição dentro do parênteses
console.log(nome[0]);

//Percorre todas as letras até a quantidade máxima no nome inserido
for (let i = 0; i < nome.length; i++) {
    console.log(nome[i]);    
}

//Percorre uma lista até 9
for (let i = 0; i < 10; i++) {
    console.log(i);    
}